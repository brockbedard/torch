export function playSvg(id,color){
  var d=PD[id];
  if(!d) return '<div style="width:60px;height:50px;"></div>';
  var dots=d[0],lines=d[1],dashes=d[2];
  var svg='<svg viewBox="0 0 60 50" width="60" height="50" xmlns="http://www.w3.org/2000/svg" style="display:block;">';
  // dashes
  for(var i=0;i<dashes.length;i++){
    var dl=dashes[i];
    if(dl.length===4) svg+='<line x1="'+dl[0]+'" y1="'+dl[1]+'" x2="'+dl[2]+'" y2="'+dl[3]+'" stroke="'+color+'" stroke-width="1" stroke-dasharray="3,2" opacity="0.5"/>';
    else if(dl.length===6) svg+='<polyline points="'+dl[0]+','+dl[1]+' '+dl[2]+','+dl[3]+' '+dl[4]+','+dl[5]+'" fill="none" stroke="'+color+'" stroke-width="1" stroke-dasharray="3,2" opacity="0.5"/>';
  }
  // lines
  for(var j=0;j<lines.length;j++){
    var ln=lines[j];
    if(ln.length===4) svg+='<line x1="'+ln[0]+'" y1="'+ln[1]+'" x2="'+ln[2]+'" y2="'+ln[3]+'" stroke="'+color+'" stroke-width="1.5" opacity="0.8"/>';
    else if(ln.length===6) svg+='<polyline points="'+ln[0]+','+ln[1]+' '+ln[2]+','+ln[3]+' '+ln[4]+','+ln[5]+'" fill="none" stroke="'+color+'" stroke-width="1.5" opacity="0.8"/>';
  }
  // dots
  for(var k=0;k<dots.length;k++){
    var r=k===0?3.5:2.5;
    svg+='<circle cx="'+dots[k][0]+'" cy="'+dots[k][1]+'" r="'+r+'" fill="'+color+'" opacity="'+(k===0?'1':'0.7')+'"/>';
  }
  svg+='</svg>';
  return svg;
}
